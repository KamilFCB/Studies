void spi_init() {
	// ustaw piny MOSI, SCK i ~SS jako wyjscia
	DDRB |= _BV(DDB3) | _BV(DDB5) | _BV(DDB2);
	// wlacz SPI w trybie master z zegarem 250 kHz
	SPCR = _BV(SPE) | _BV(MSTR) | _BV(SPR1);
}

void timer_init() {
	// prescaler 64
	TCCR2B = _BV(CS22);
}

// transfer jednego bajtu
void spi_transfer(uint8_t data) {
	SPDR = data;
	// czekaj na ukonczenie transmisji
	while (!(SPSR & _BV(SPIF)));
	// wyczysc flage przerwania
	SPSR |= _BV(SPIF);
}

int main() {
	timer_init();
	spi_init();
	PORTB &= ~_BV(SS);
	while (1) {
		for(uint16_t i = 0; i < SOUND_LENGTH; i++) {
			while (TCNT2 < 32);
			TCNT2 = 0;
			PORTB &= ~_BV(SS);
			uint8_t sample = pgm_read_byte(&sound[i]);
			spi_transfer(0b00110000 | (sample >> 4));
			spi_transfer(sample << 4);
			PORTB |= _BV(SS);
		}		
	}
}

